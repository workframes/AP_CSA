public class MathTester {
    public static void main(String[] args){
        int absolute1 = Math.abs(100); // Returns 100
        System.out.println(absolute1);

        double powerOf2 = Math.pow(2.0, 7.0); // Returns 128.0
        System.out.println(powerOf2);

        double squareOf49 = Math.sqrt(49.0); // Returns 7.0
        System.out.println(squareOf49);

    }
}
