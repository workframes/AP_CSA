/*
* Purpose: Demonstrate casting using division
*/

public class CastingNumbers {
    public static void main(String[] args){
        System.out.println(6 / 4); // Retuns Int
        System.out.println(6.0 / 4); // Retuns Double
        System.out.println(6 / 4.0); // Returns Double
        System.out.println((double) 6/4); // Return Double
    }
}
