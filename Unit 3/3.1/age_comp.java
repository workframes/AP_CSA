public class age_comp {
    public static void main(String[] args){
        int myAge = 15;
        int jwAge = 18;

        if(myAge > jwAge){
            System.out.println("My age is greater than JW's age");
        }
        else if(myAge < jwAge){
            System.out.println("JW's age is greater than my age");
        }
        else{
            System.out.println("My age is equal to JW's age");
        }
    }
}
